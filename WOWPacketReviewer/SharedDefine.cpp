String              GetFieldName(int fieldtype, int input)
{
    String result;
    result = GetEObjectFieldsName(input);
    if(result != "")
        return      result;

    switch(fieldtype)
    {
        case    TYPEID_OBJECT:
            return  GetEObjectFieldsName(input);
        case    TYPEID_ITEM:
		case    TYPEID_CONTAINER:
			{
				if(input >= ITEM_END)
				{
					return  GetEContainerFieldsName(input);
				}
				else
				{
					return	GetEItemFieldsName(input);
				}
			}
        case    TYPEID_UNIT:
            return  GetEUnitFieldsName(input);          
        case    TYPEID_PLAYER:
            return  GetEUnitFieldsName(input);
        case    TYPEID_GAMEOBJECT:
            return  GetEGameObjectFieldsName(input);
        case    TYPEID_DYNAMICOBJECT:
            return  GetEDynamicObjectFieldsName(input);
        case    TYPEID_CORPSE:
            return  GetECorpseFieldsName(input);
    }
    return      "";
}

